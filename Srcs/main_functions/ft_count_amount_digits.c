#include "../../Headers/libft.h"

int		ft_count_amount_digits(int num)
{
	int	d;

	d = 0;
	if (num <= 0)
	{
		d = 1;
		num *= -1;
	}
	while (num > 0)
	{
		num /= 10;
		d++;
	}
	return (d);
}
